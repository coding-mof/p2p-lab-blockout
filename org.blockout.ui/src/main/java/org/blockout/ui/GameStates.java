package org.blockout.ui;

/**
 * Enumeration for all game states. The ordinal of the enumeration values are
 * used as ids.
 * 
 * @author Marc-Christian Schulze
 * 
 */
public enum GameStates {
	/**
	 * Shows the start screen and the menus.
	 */
	StartMenu,
	/**
	 * Renders the actual game.
	 */
	InGame
}
